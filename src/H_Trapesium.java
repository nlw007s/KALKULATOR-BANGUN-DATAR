public abstract class H_Trapesium {
    int t;
    int sisi_bawah;
    int sisi_atas;
    int hasil;

    public int getHasil() {
        return hasil;
    }

    public void setHasil(int hasil) {
        this.hasil = hasil;
    }

    public int getT() {
        return t;
    }

    public void setT(int t) {
        this.t = t;
    }

    public int getSisi_bawah() {
        return sisi_bawah;
    }

    public void setSisi_bawah(int sisi_bawah) {
        this.sisi_bawah = sisi_bawah;
    }

    public int getSisi_atas() {
        return sisi_atas;
    }

    public void setSisi_atas(int sisi_atas) {
        this.sisi_atas = sisi_atas;
    }
}
